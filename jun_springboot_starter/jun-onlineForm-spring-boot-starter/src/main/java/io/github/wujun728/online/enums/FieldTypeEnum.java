package io.github.wujun728.online.enums;

/**
 * 字段类型枚举类
 * 定义了数据库支持的字段类型
 */
public enum FieldTypeEnum {
    
    /**
     * 字符串类型
     */
    String,
    
    /**
     * 长整型
     */
    Long,
    
    /**
     * 整型
     */
    Integer,
    
    /**
     * 双精度浮点型
     */
    Double,
    
    /**
     * 日期类型
     */
    Date,
    
    /**
     * 日期时间类型
     */
    DateTime,
    
    /**
     * 高精度小数类型
     */
    BigDecimal,
    
    /**
     * 位类型
     */
    Bit,
    
    /**
     * 文本类型
     */
    Text
    
}

